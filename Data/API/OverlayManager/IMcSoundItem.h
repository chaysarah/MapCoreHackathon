	/// \param[out]	pfSeconds					The sound's last time point value.
	///
	/// \return
	///     - status result
	//==============================================================================
	virtual IMcErrors::ECode GetCurrTimePoint(IMcMapViewport *pMapViewport, IMcObject *pObject, 
		float *pfSeconds) const = 0;

	//==============================================================================
	// Method Name: GetCurrTimePoint()
	//------------------------------------------------------------------------------
	/// Retrieves the sound's time point value for the specific object
